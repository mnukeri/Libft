#include "libft.h"

int		ft_wc(const char *str, char c)
{
	int p;
	int k;

	p = 0;
	k = 0;
	while (str[p])
	{
		if (str[p] == c && str[p - 1] != c)
			k++;
		p++;
	}
	return (k);
}
