#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int p;
	int k;

	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n == 2147483647)
	{
		ft_putstr("2147483647");
		return ;
	}
	if (n >= 0)
	p = n;
	else
	{
		write(fd, "-", 1);
		p = n * - 1;
	}
	if (p > 9)
	{
		ft_putnbr_fd(p / 10, fd);
	}
	k = p % 10 + '0';
	write(fd, &k, sizeof(k));
}
