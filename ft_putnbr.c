#include "libft.h"

void	ft_putnbr(int n)
{
	int 	p;
	int 	k;

	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n == 2147483647)
	{
		ft_putstr("2147483647");
		return ;
	}
	if (n >= 0)
	p = n;
	else
	{
		write(1, "-", 1);
		p = n * - 1;
	}
	if (p > 9)
	{
		ft_putnbr(p / 10);
	}
	k = p % 10 + '0';
	write(1, &k, sizeof(k));
}
