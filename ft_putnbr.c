#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int p;
	unsigned int k;

	p = 0;
	if (n >= 0)
		p = n;
	if (n < 0)
	{
		p = n * -1;
		write(1, "-", 1);
	}
	if (p > 9)
	{
		ft_putnbr(p / 10);
	}
	k = p % 10 + '0';
	write(1, &k, sizeof(k));
}
